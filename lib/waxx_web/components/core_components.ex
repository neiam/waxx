  """
  attr :name, :string, required: true
  attr :class, :any, default: "size-4"

  def icon(%{name: "hero-" <> _} = assigns) do
    ~H"""
    <span class={[@name, @class]} />
    """
  end

  ## JS Commands

  def show(js \\ %JS{}, selector) do
    JS.show(js,
      to: selector,
      time: 300,
      transition:
        {"transition-all ease-out duration-300",
         "opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95",
         "opacity-100 translate-y-0 sm:scale-100"}
    )
  end

  def hide(js \\ %JS{}, selector) do
    JS.hide(js,
      to: selector,
      time: 200,
      transition:
        {"transition-all ease-in duration-200", "opacity-100 translate-y-0 sm:scale-100",
         "opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95"}
    )
  end

  @doc """
  Translates an error message using gettext.
  """
  def translate_error({msg, opts}) do
    # When using gettext, we typically pass the strings we want
    # to translate as a static argument:
    #
    #     # Translate the number of files with plural rules
    #     dngettext("errors", "1 file", "%{count} files", count)
    #
    # However the error messages in our forms and APIs are generated
    # dynamically, so we need to translate them by calling Gettext
    # with our gettext backend as first argument. Translations are
    # available in the errors.po file (as we use the "errors" domain).
    if count = opts[:count] do
      Gettext.dngettext(WaxxWeb.Gettext, "errors", msg, msg, count, opts)
    else
      Gettext.dgettext(WaxxWeb.Gettext, "errors", msg, opts)
    end
  end

  @doc """
  Translates the errors for a field from a keyword list of errors.
  """
  def translate_errors(errors, field) when is_list(errors) do
    for {^field, {msg, opts}} <- errors, do: translate_error({msg, opts})
  end

  @doc """
  A color input with a grid of tailwind swatches and a native color picker.

  The form field stays a plain text input holding the resolved hex value;
  swatches and the native picker write into it via a colocated JS hook
  and dispatch `input`/`change` events so LiveView validators and
  `phx-change` still fire.

      <.color_picker field={@stage_form[:color]} label="Color" />
  """
  attr :field, Phoenix.HTML.FormField, required: true
  attr :label, :string, default: "Color"

  def color_picker(assigns) do
    ~H"""
    <div
      id={"#{@field.id}-picker"}
      data-target={@field.id}
      phx-hook=".PickColor"
      class="form-control"
    >
      <label class="label-text text-sm mb-1">{@label}</label>

      <div class="flex items-center gap-2">
        <span
          data-color-preview
          class="size-8 rounded border border-base-content/20 shrink-0"
          style={"background: #{@field.value || "transparent"}"}
        />
        <input
          type="text"
          name={@field.name}
          id={@field.id}
          value={@field.value}
          placeholder="#22c55e"
          autocomplete="off"
          spellcheck="false"
          class="input input-sm input-bordered w-32 font-mono"
        />
        <input
          type="color"
          data-color-source
          value={normalize_hex(@field.value) || "#888888"}
          aria-label="Custom color"
          class="size-8 rounded border border-base-content/20 cursor-pointer bg-transparent"
        />
        <button
          type="button"
          data-color-clear
          class="btn btn-ghost btn-xs"
          aria-label="Clear color"
          title="Clear"
        >
          <.icon name="hero-no-symbol-micro" class="size-4" />
        </button>
      </div>

      <div class="grid grid-cols-10 gap-1 mt-2 max-w-fit">
        <button
          :for={color <- tailwind_swatches()}
          type="button"
          data-color-swatch
          data-color={color}
          style={"background: #{color}"}
          aria-label={"Set color to #{color}"}
          title={color}
          class="size-5 rounded border border-base-content/20 hover:scale-110 transition-transform focus:outline focus:outline-2 focus:outline-primary"
        />
      </div>

      <script :type={Phoenix.LiveView.ColocatedHook} name=".PickColor" phx-no-curly-interpolation>
        export default {
          mounted() {
            const root = this.el;
            const target = document.getElementById(root.dataset.target);
            if (!target) return;

            const preview = root.querySelector("[data-color-preview]");
            const native = root.querySelector("[data-color-source]");

            const isHex = (v) => typeof v === "string" && /^#[0-9a-fA-F]{6}$/.test(v);

            const setColor = (color) => {
              target.value = color;
              if (preview) preview.style.background = color || "transparent";
              if (native && isHex(color)) native.value = color;
              target.dispatchEvent(new Event("input", { bubbles: true }));
              target.dispatchEvent(new Event("change", { bubbles: true }));
            };

            root.querySelectorAll("[data-color-swatch]").forEach(btn => {
              btn.addEventListener("click", () => setColor(btn.dataset.color));
            });

            if (native) {
              native.addEventListener("input", e => setColor(e.target.value));
            }

            const clear = root.querySelector("[data-color-clear]");
            if (clear) {
              clear.addEventListener("click", () => setColor(""));
            }

            // Reflect manual typing into the preview/native picker.
            target.addEventListener("input", () => {
              if (preview) preview.style.background = target.value || "transparent";
              if (native && isHex(target.value)) native.value = target.value;
            });
          }
        }
      </script>
    </div>
    """
  end

  # Tailwind's mid-saturation palette (500 shade) — a usable signal of
  # what "red", "blue", etc look like. Plus the neutral grays at the end.
  defp tailwind_swatches do
    ~w(
      #ef4444 #f97316 #f59e0b #eab308 #84cc16
      #22c55e #10b981 #14b8a6 #06b6d4 #0ea5e9
      #3b82f6 #6366f1 #8b5cf6 #a855f7 #d946ef
      #ec4899 #f43f5e #64748b #6b7280 #71717a
    )
  end

  defp normalize_hex(v) when is_binary(v) do
    if Regex.match?(~r/^#[0-9a-fA-F]{6}$/, v), do: v, else: nil
  end

  defp normalize_hex(_), do: nil
end
